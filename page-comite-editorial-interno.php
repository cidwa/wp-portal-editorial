                        <h4 class="margen-h-body">Políticas de Publicación</h4>
                        <p class="margen-h-body">Las publicaciones deberán ser congruentes con los objetivos y las funciones del área o disciplina de adscripción del docente y propias de la Facultad de Estudios Superiores Acatlán con los señalamientos del plan de desarrollo atendiendo preferentemente los siguientes aspectos:</p>
