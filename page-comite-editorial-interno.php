                        <p class="margen-h-body">Los materiales que se señalan a continuación, impresos o electrónicos, deberán ser sometidos a consideración de los Comités Editoriales Internos y posteriormente enviados al Comité Editorial de la Facultad. En los casos de impresiones bastará con el visto bueno del Secretario Ejecutivo del Comité Editorial de la Facultad, siempre y cuando la edición se encuentre agotada.</p>
