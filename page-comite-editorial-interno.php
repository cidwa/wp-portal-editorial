                        <h4 class="panel-title">
                            VI. CRITERIOS PARA DICTAMEN DE TRABAJOS
                        </h4>
                    </a>
                </div>
                <div id="collapseSix" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingSix">
                    <div class="panel-body">

                        <h4 class="margen-h-body">Normas Generales</h4>
                        <p class="margen-h-body">Se propone estandarizar los procesos y criterios de dictamen para los trabajos, bajo una modalidad única que deberá ser respetada, consistente en los siguientes puntos:</p>
                        <ul class="margen-panel-body">
                            <li>Utilización de un formato único para dictamen editorial, según el tipo de trabajo.</li>
                            <li>Se establecerá como tiempo límite para la realización de dictámenes, 30 días hábiles.</li>
                            <li>En caso de incumplimiento de las fechas señaladas, la edición de la obra se verá afectada en el tiempo para su publicación.</li>
                            <li>En caso de que no se entreguen los dictámenes se nombrarán a nuevos dictaminadores.</li>
                        </ul>
                        <h4 class="margen-h-body">Dictaminadores y criterios para publicación</h4>
                        <p class="margen-h-body">Todos los documentos deberán ser sometidos a la evaluación del Comité Editorial Interno para su edición.</p>
                        <p class="margen-h-body">En el caso de otros materiales de apoyo a la docencia que no tengan características de libro, como apuntes, series de ejercicios, manuales de comprensión de lectura, folletos y boletines de información estadística, se requerirá que sean dictaminados por el Comité Editorial Interno, tomando sus acuerdos por mayoría de votos de los representantes presentes en la sesión. Dichos materiales deberán venir acompañados del protocolo para la valoración, debidamente requisitado, así como la opinión razonada del Jefe del Programa o Coordinador respectivo en la que indique a que curso o materia apoyarán dichos materiales y la justificación de la necesidad de su publicación, sugiriendo el tiraje necesario para cubrir las necesidades del curso o la materia.</p>
                        <p class="margen-h-body">Para la aprobación de libros se requerirá que sean dictaminados por tres avales, dos de los cuales serán externos. Cuando exista un dictamen desfavorable o negativo el material se devolverá al autor para que decida si hace o no las modificaciones sugeridas. Si es el caso, el material modificado será enviado nuevamente al dictaminador; si el dictamen continúa siendo negativo el material se turnará al Comité Editorial de la Facultad quien tomará una decisión al respecto.</p>
                        <p class="margen-h-body">Los libros producto de proyectos institucionales –como Programa de Apoyo a Proyectos de Investigación e Innovación Tecnológica (PAPIIT) o Programa de Apoyo a Proyectos para la Innovación y Mejoramiento de la Enseñanza (PAPIME)— que requieran apoyo económico de la FES Acatlán, para su aprobación requieren de tres dictaminadores: dos externos y uno interno.</p>
                        <p class="margen-h-body">Los libros, producto de proyectos institucionales –como PAPIIT o PAPIME— que no requieran apoyo económico, para su aprobación requieren de dos dictaminadores internos propuestos por los autores.</p>
                        <p class="margen-h-body">En el caso de los productos de investigación, materiales de apoyo a la docencia, guías de estudio, prácticas de laboratorios, antologías y/o lecturas selectas, manuales y materiales de apoyo a las clases de idiomas y documentos de divulgación se requerirán dos dictaminadores internos.</p>
                        <p class="margen-h-body">Para la designación de los avales, el Presidente del Comité Editorial Interno, solicitará a los miembros de éste una propuesta de dictaminadores afín a la temática que se trate en la obra sujeta de dictamen, misma que deberá ser aprobada por consenso. No será necesario en esta ocasión presentar el citado material.</p>
                        <h4 class="margen-h-body">Documentación presentada por los dictaminadores</h4>
                        <p class="margen-h-body">El dictamen de las antologías o materiales considerados como libros deberá ser individual, ampliamente fundamentado y detallado en el que se expliquen exhaustivamente los motivos por los cuales se recomienda la edición de la obra analizada. Dicho dictamen corresponderá rigurosamente, incluso en fechas, con el material que se presente al Comité Editorial de la Facultad.</p>
                        <p class="margen-h-body">El dictamen respetará los siguientes puntos:</p>
                        <ul class="margen-panel-body">
                            <li>1. Opinión General</li>
                            <li>2. Contribución más importante del documento</li>
                            <li>3. Limitaciones serias del documento</li>
                            <li>4. Opinión sobre:
                                <ul class="margen-panel-body">
                                    <li>Calidad</li>
                                    <li>Redacción</li>
                                    <li>Extensión</li>
                                    <li>Contribución al entendimiento del área de trabajo</li>
                                    <li>Metodología</li>
                                    <li>Bibliografía</li>
                                </ul>
                            </li>
                            <li>5. Pertinencia de su publicación por parte de la Facultad de Estudios Superiores Acatlán
                                <ul class="margen-panel-body">
                                    <li>• ¿Cuál es su originalidad con relación a obras semejantes?</li>
                                    <li>• ¿A que público estaría destinado dicho documento?</li>
                                    <li>?? Estudiantes</li>
                                    <li>?? Profesores</li>
                                    <li>?? Público en General</li>
                                    <li>• ¿Tendría el documento un mercado importante en México?</li>
                                    <li>• ¿Que vigencia tendría la obra?</li>
                                    <li>• ¿Considera usted que el título y los subtítulos obedecen al contenido que encabezan?</li>
                                    <li>• ¿Propondría usted un título y/o subtítulos más adecuados?</li>
                                    <li>• ¿Conoce usted un texto que pudiera sustituir al que se le ha dado a dictaminar?</li>
                                </ul>
                            </li>
                            <li>6. ¿Recomendaría usted su publicación? Sí o no.</li>
                            <li>Si su respuesta es negativa que cambios sugeriría.</li>
                        </ul>
                        <p class="margen-h-body">En el caso de materiales de apoyo a la docencia, el dictamen que emite el Comité Editorial Interno deberá ser razonado, explicando exhaustivamente los motivos por los cuales se recomienda su publicación, sin olvidar mencionar la asignatura, la carrera y el número de los alumnos beneficiados con esta publicación. A la solicitud deberá anexarse el acuerdo firmado por todos los miembros del Comité.</p>

