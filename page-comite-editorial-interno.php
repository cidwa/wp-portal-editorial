                <div id="collapseFour" class="panel-collapse collapse" role="tabpanel" aria-labelledby="headingFour">
                    <h4 class="margen-h-body">Normas</h4>
                    <div class="margen-panel-body">
                        <p>El autor solicitará a su órgano de adscripción (División o Coordinación) la edición de su obra, debiendo acompañarla de un oficio en el que manifieste que la obra está totalmente terminada y que está de acuerdo con lo que se va a editar.</p>
                        <p>La obra deberá presentarse en archivo electrónico con una impresión de su contenido, observando los siguientes puntos:</p>
                    </div>
                    <h4 class="margen-h-body">Impresiones</h4>
                    <div class="margen-panel-body">
                        <p>Deberán ser legibles. Es decir, no tendrán que ser grises ni demasiado oscuras (saturadas).</p>
                        <p>No deberán presentar letras “rotas” o “ilegibles”.</p>
                        <p>Las páginas deberán aparecer completas sin que las orillas o extremos queden fuera de los márgenes, ilegibles o incompletas.</p>
                        <p>Se presentarán sueltas y numeradas (sin engargolar).</p>
                        <p>No forzar el espacio de la hoja tratando de incluir dos páginas en una sola, ya que existe la posibilidad de que no aparezcan perfectamente en su totalidad las dos páginas.</p>
                        <p>En el caso de Antologías se deberán incluir las fichas bibliográficas de donde se extrajo el material, la referencia del autor y el índice en el que se establezca el orden en que se armará el material.</p>
                    </div>
                    <h4 class="margen-h-body">Versión electrónica: disco compacto para PC</h4>
                    <ul class="margen-panel-body">
                        <li>Se deberá utilizar el procesador de texto Word para Windows versión 97-2003.</li>
                        <li>En caso de versiones más recientes aplicar la opción de compatibilidad de archivos versión 97-2003.</li>
                        <li>El texto capturado se hará continuo, marcando retorno solamente en “punto y aparte”.</li>
                        <li>Se podrán incluir jerarquías de texto (itálicas, negritas, etc.).</li>
                        <li>El centrado de los títulos, deberá hacerse con el modo de “alineación centrada” (no con tabuladores, ni con espacios).</li>
                        <li>El tipo de letra debe ser Arial de 12 puntos con interlineado de 1.5.</li>
                        <li>Alinear el cuerpo de texto a la derecha (evitar la alineación justificada)</li>
                        <li>No sangrar los inicios de párrafo.</li>
                        <li>Se deberá evitar el uso de saltos de página.</li>
                        <li>Usar márgenes: superior e inferior 2.5 cm, izquierdo y derecho 3.0 cm.</li>
                        <li>El tamaño de la página deberá ser tamaño carta -letter- (no usar A-4, u oficio).</li>
                        <li>Se integrará una carpeta con el nombre del libro y contendrá un archivo por cada capítulo del libro. El nombre del archivo deberá incluir el numeral que ocupe el capítulo dentro del orden asignado.</li>
                    </ul>
                    <p class="margen-h-body">Ejemplo: del libro: La independencia de Texas.</p>
                    <p class="margen-h-body">Ejemplo: Carpeta: La independencia de Texas</p>
                    <ul class="list-unstyled margen-panel-body">
                        <li>01 Tex índice</li>
                        <li>02 Tex introducción</li>
                        <li>03 Tex presentación</li>
                        <li>04 Tex capítulo 1</li>
                        <li>05 Tex capítulo 2</li>
                        <li>06 Tex capítulo 3</li>
                    </ul>
                    <ul class="margen-panel-body">
                        <li>Se podrán marcan números superíndices y subíndices, solamente con el modo de –“efecto de fuente”-.</li>
                        <li>Se podrán incluir pies de página dentro del texto utilizando las herramientas del procesador.</li>
                        <li>En caso de que se incluyan citas del texto de más de cuatro líneas se distinguirán marcando dos retornos (espacios de línea) antes de iniciar la cita y otros dos al finalizar.</li>
                    </ul>
