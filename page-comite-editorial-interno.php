                        <ul class="margen-panel-body">
                            <li> 17.0 x 10.5 cm (1/4 de Oficio). Dimensiones para minilibros con extensión máxima de 80 y mínima de 40 cuartillas.</li>
                            <li> 13.0 x 18.0 cm (series de humanidades)</li>
                            <li> 14.0 x 21.5 cm (1/2 Carta).</li>
